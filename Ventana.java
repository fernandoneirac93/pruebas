        //METODO PARA CAPTURAR LA PANTALLA, SOLO CAPTURA LA ZONA DE LA TABLA,
        //QUE SE INDICA DEBAJO DE TODO EN LA LLAMADA AL METODO guardarActionPerformed.
    void capturarPantalla(Component component) {
    Rectangle rect = component.getBounds();
 
    try {
        String format = "jpg";
        component.setName("listaClase");
        String fileName = component.getName() + "." + format;
        BufferedImage captureImage =
                new BufferedImage(rect.width, rect.height,   //ESTO CAPTURA EL ANCHO Y ALTO DEL 
                                                             // FRAME DE LA TABLA
                        
                                    BufferedImage.TYPE_INT_RGB);//-->ESTO HACE QUE LA IMAGEN
                                                                //SE IMPRIMA CON FORMATO RGB.
        component.paint(captureImage.getGraphics());//CAPTURA LA ZONA DEFINIDA ARRIBA.
 
        ImageIO.write(captureImage, format, new File(fileName));//PARAMETROS DEFINIDOS ARRIBA.
 
    } catch (IOException ex) {
        System.err.println(ex);
    }
}

    
    
